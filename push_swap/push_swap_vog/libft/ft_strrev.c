#include "libft.h"

char	*ft_strrev(char *str)
{
	char	u;
	int		i;
	int		n;

	i = 0;
	n = 0;
	while (str[n] != '\0')
		n++;
	while (i < (n / 2))
	{
		u = str[i];
		str[i] = str[n - i - 1];
		str[n - i - 1] = u;
		i++;
	}
	return (str);
}
